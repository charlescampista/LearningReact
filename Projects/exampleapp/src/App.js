import React, { Component } from 'react';
import './App.css';
import Person from './Person/Person';

class App extends Component {
  state = {
    person: [
      {name: 'Max', age: 28},
      {name: 'Manu', age: 29},
      {name: 'Stephanie', age: 26}
    ]
  }

  swhitchNameHandler = () => {
    this.setState({
      person: [
        {name: 'Maximilian', age: 28},
        {name: 'Manu', age: 29},
        {name: 'Stephanie', age: 27}
      ]
    });
  }

  render() {
    return (
      <div className="App">
        <h1>Hello World!</h1>
        <p>THIS IS REALLY WORKING!</p>
        <button onClick={this.swhitchNameHandler}>Switch Name</button>
        <Person name={this.state.person[0].name} age={this.state.person[0].age}/>
        <Person name={this.state.person[1].name} age={this.state.person[1].age}/>
        <Person name={this.state.person[2].name} age={this.state.person[2].age}>Argumento entre tags</Person>
      </div>
    ); 
  }
}

export default App;
